CREATE TABLE CLUB
(
	CLUBID INT PRIMARY KEY,
	CLUBNAME NVARCHAR(100) NOT NULL,
	CLUBSHORTNAME VARCHAR(6) UNIQUE NOT NULL,
	ESTABLISHEDYEAR INT NOT NULL,
	HOMEFIELD NVARCHAR(100) NOT NULL
