GO
CREATE PROC DSGHIBAN
@START DATE, @END DATE
AS
BEGIN
	SELECT NAME, CLUBNAME,PLAYERTYPENAME, COUNT(*) AS TOTALGOAL
	FROM PLAYER PL, CLUB CL, PLAYERTYPE PT, GOAL GL, MATCH MA
	WHERE PL.CLUBID=CL.CLUBID AND PL.PLAYERTYPEID = PT.PLAYERTYPEID AND PL.PLAYERID = GL.PLAYERID AND GL.MATCHID = MA.MATCHID AND MA.MATCHDAY<@END AND MA.MATCHDAY>@START
	GROUP BY NAME, CLUBNAME, PLAYERTYPENAME
END

