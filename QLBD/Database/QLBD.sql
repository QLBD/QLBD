	PLAYERID INT REFERENCES PLAYER(PLAYERID) ON DELETE CASCADE ON UPDATE CASCADE NOT NULL,
	GOALTYPEID INT REFERENCES GOALTYPE(GOALTYPEID) NOT NULL,
	GOALTIME INT NOT NULL
